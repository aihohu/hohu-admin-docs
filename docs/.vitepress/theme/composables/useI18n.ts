export function useI18n() {
  const { lang } = useData();
  const locale = computed(() => (lang.value === 'zh-CN' ? 'zh' : 'en'));
  const t = computed(() => translations[locale.value]);
  const prefix = computed(() => (locale.value === 'zh' ? '/zh' : ''));
  return { t, prefix, locale };
}
